    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String usuario =txt_user.getText();
        String contrasena = txt_password.getText();
        
        IUsuarioDao usDAO = new UsuarioDaoImpl();
        Usuario userLogin = usDAO.login(usuario, contrasena);
        
        if(userLogin == null){
        JOptionPane.showMessageDialog(null, "usuario no registrado:( ", "INFORMATION_MESSAGE", JOptionPane.INFORMATION_MESSAGE);
        
        }else if(userLogin.getUser().equals(usuario) && userLogin.getPassword().equals(contrasena) ){
           //establecer ssesion del usuario
            UserSession.getInstance().setUser(userLogin);
            home init = new home();
            init.setVisible(true);
            init.setLocationRelativeTo(null);
            this.setVisible(false);
        }else{
            home init = new home();
            init.setVisible(true);
            init.setLocationRelativeTo(null);
            this.setVisible(false);  
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

    

